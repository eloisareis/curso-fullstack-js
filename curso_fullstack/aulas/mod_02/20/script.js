// aula 20 - Operadores de Comparação 
// > maior que
// < menor que
// >= maior ou igual a
// <= menor ou igual a
// == igualdade (valor)
// === igualdade estrita (valor e tipo)
// != diferente (valor)
// !== diferente estrito (valor e tipo)

console.log(10 > 5); // true
console.log(10 < 5); // false
console.log(10 >= 10); // true
console.log(10 <= 9); // false
console.log(10 == '10'); // true (comparação de valor, tipo diferente) (ele faz uma coersão de tipo)
console.log(10 === '10'); // false (comparação de valor e tipo)
console.log(10 != '10'); // false (comparação de valor, tipo diferente)
console.log(10 !== '10'); // true (comparação de valor e tipo)