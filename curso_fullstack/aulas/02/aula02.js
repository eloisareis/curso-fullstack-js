// Console.log - A primeira coisa que você vai ver em JS

console.log('Hello Word!'); // método log dentro da função console
// ponto e virgula é opcional em JS
// Dentro do log podemos usar variaveis, objetos, valores literais ect.

console.log("Hello Word!"); // essa função quebra a linha automaticamente
// As duas primeiras funções funcionam tanto com aspas simples e duplas.
// Isso proque caso dentro da função deva ter alguma aspa o programador pode usar uma delas para indicar os valores literais e com a outra
// indicar o campo com aspas por exemplo:
console.log("Eloisa 'Reis' linda"); // o valor literal é em aspas duplas e a parte com aspas no texto esta em aspas simples
// é sempre uma ou a outra, nunca as duas juntas.

console.log(`'Eloisa' "Reis"`) // neste eu posso usar tanta aspas simples ou duplas

// entre crase, aspas duplas ou simples é um tipo STRING = texto

console.log(10, 5.5, 'Eloisa Reis'); // exibindo um valor inteiro, real - Number = Número